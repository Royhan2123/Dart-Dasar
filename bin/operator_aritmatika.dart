void main(){

  var result = 10 + 10;
  var result2 = 10 - 10;
  var result3 = -5 + 5;
  var result4 = 5;
  var result5 = 5 * 5;
  // 10 / bagi 3 = 3.33333 berupa double  (/) <= pembagian
  var result6 = 10 / 3;
  // 10 / bagi 3 = 3.33333 berupa int  (~/) <= pembagian juga cuman berupa int
  var result7 = 10 ~/ 3;
  // 1 % 2 = 1,adalah sisa bagi dari 1 pembagian 2,jadi 1 di bagi 2 kan gabisa,
  // jadi 1 * 1 = 1 di kurang 2 = 1;
  var result8 = 1 % 2;

  print(result);
  print(result2);
  print(result3);
  print(result4);
  print(result5);
  print(result6);
  print(result7);
  print(result8);

}
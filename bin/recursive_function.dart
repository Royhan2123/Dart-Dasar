int factorialLopp(int value){
var result = 1;
for(var i = 1; i<= value; i++){
  result *=i;
}
return result;
}
/// RECURSIFE FUNCTION ADALAH FUNCTION YANG MEMANGGIL DIRIINYA SENDIRI
/// BEDANNYA DENGAN HIGHER FUNCTION ADALAH,KALAU HIGHER FUNCTION
/// BISA MEMANGGIL DUA FUNCTION KETIKA DI JALANKAN
/// RECURSIFE FUNCTION AKAN MEMANGIL DIRINYA SENDIRI KETIKA DI JALANKAN SEPERTI CONTOH DI BAWAH:
/// KITA MEMBUAT SEBUAH FUNCTION INT DENGAN TIPE DATA INT VARIABEL NUMBER
/// LALU DI DALAMNYA KITA AKAN MEMBUAT SEBUAH PERKALIAN YANG DIMANA ISINYA ADALAH
/// JIKA NUMBER NYA == 1,MAKA KITA AKAN KEMBALIKAN NILAI NYA KE 1,KARENA 1 * 1 MAKA HASILNYA 1;
/// LALU JIKA TIDAK ATAU ELSE,MAKA KITA AKAN MERETURN NILAI NUMBER * (VARIABEL FUNCTION)(NUMBER - 1);
/// MAKSUD DI KURANG SATU IALAH
int recursifeFunction(int number){
  if(number == 1){
  return 1;
  }else{
   return number * recursifeFunction(number - 1);
  }
}

void main(){
  print(factorialLopp(10));
  print(recursifeFunction(10));
}